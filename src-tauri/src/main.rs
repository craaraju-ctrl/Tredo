#[tauri::command]
async fn trigger_orchestra_cycle(state: State<'_, Mutex<AppState>>) -> Result<String, String> {
    println!("[Tauri] === FULL ORCHESTRA CYCLE TRIGGERED FROM UI ===");

    // Clone the orchestrator out of the lock first, then release the lock
    let orch_clone = {
        let app_state = state.lock().await;
        app_state.orchestrator.clone()
    };

    if let Some(orch) = orch_clone {
        let dir = TradeDirection::Long;
        let sym = "NIFTY";
        let entry = 24500.0;
        let stop = 24200.0;
        let target = 25000.0;

        // Agentic call — only the symbol. The Tredo agent decides direction and its own levels
        // from market data + full analysis (indicators, debate, memory, rules).
        match orch.run_full_pipeline(&sym).await {
            Ok(summary) => {
                let action = summary
                    .final_signal
                    .map(|s| {
                        format!(
                            "{} {:.2}",
                            if s.direction == TradeDirection::Long {
                                "BUY"
                            } else {
                                "SELL"
                            },
                            s.entry_price
                        )
                    })
                    .unwrap_or_else(|| "HOLD".to_string());
                Ok(format!(
                    "ORCHESTRA CYCLE COMPLETE | Action: {} | Reason: {} | Duration: {}ms",
                    action, summary.reason, summary.total_duration_ms
                ))
            }
            Err(e) => Ok(format!("ORCHESTRA CYCLE ERROR: {}", e)),
        }
    } else {
        Ok("ORCHESTRA CYCLE COMPLETE | No in-process orchestrator — run start_autonomous_system first".to_string())
    }
}

// ── Main ─────────────────────────────────────────────────────────────────────
#[tokio::main]
async fn main() {
    env_logger::init_from_env(env_logger::Env::default().default_filter_or("info"));
    log::info!("[tredo UI] Starting Tauri application...");

    // Initialize in-process orchestrator
    let orch = initialize_autonomous_system()
        .await
        .expect("Failed to initialize autonomous orchestrator");

    let app_state = AppState {
        orchestrator: Some(orch),
        ..AppState::new()
    };

    tauri::Builder::default()
        .plugin(tauri_plugin_shell::init())
        .manage(Mutex::new(app_state))
        .invoke_handler(tauri::generate_handler![
            get_system_status,
            execute_trade,
            check_discipline,
            run_backtest,
            trigger_orchestra_cycle,
            fetch_live_stock_price,
            update_rules,
            start_autonomous_system,
            stop_autonomous_system,
            get_system_health,
            get_cot_chains,
        ])
        .run(tauri::generate_context!())
        .expect("error while running tauri application");
}
