// ── Process handles for spawned services ────────────────────────────────────
struct SystemProcesses {
    kronos: Option<std::process::Child>,
    orchestrator: Option<std::process::Child>,
}

impl SystemProcesses {
    fn new() -> Self {
        Self {
            kronos: None,
            orchestrator: None,
        }
    }
}

// ── Application State (holds in-process orchestrator for real COT data) ──────
struct AppState {
    config: Config,
    rules: DisciplineRules,
    execution: ExecutionEngine,
    processes: StdMutex<SystemProcesses>,
    orchestrator: Option<AutonomousOrchestrator>,
    /// Flat list of chain-of-thought entries generated by Tauri command handlers
    /// (the in-process orchestrator pushes to its own state.cot_store).
    tauri_cot_store: Arc<tokio::sync::RwLock<Vec<CotEntry>>>,
    tauri_cot_id: Arc<AtomicU64>,
}

impl AppState {
    fn new() -> Self {
        let config = Config::default();
        let rules = DisciplineRules::default();
        let memory =
            MemoryStore::new("tredo_memory.redb").expect("Failed to initialize memory store");
        let execution = ExecutionEngine::new(config.initial_balance, memory);
        Self {
            config,
            rules,
            execution,
            processes: StdMutex::new(SystemProcesses::new()),
            orchestrator: None,
            tauri_cot_store: Arc::new(tokio::sync::RwLock::new(Vec::new())),
            tauri_cot_id: Arc::new(AtomicU64::new(1)),
        }
    }
}

impl AppState {
    /// Push a COT entry generated by a Tauri command handler (not pipeline).
    async fn push_tauri_cot(
        &self,
        agent: &str,
        input: &str,
        action: &str,
        reason: &str,
        confidence: f64,
        symbol: Option<String>,
    ) {
        let id = self.tauri_cot_id.fetch_add(1, Ordering::Relaxed);
        let entry = CotEntry {
            id,
            chain_id: id,
            parent_id: None,
            agent: agent.to_string(),
            input: input.to_string(),
            action: action.to_string(),
            reason: reason.to_string(),
            confidence,
            timestamp: chrono::Utc::now().to_rfc3339(),
            symbol,
        };
        let mut store = self.tauri_cot_store.write().await;
        store.push(entry);
        if store.len() > 500 {
            store.remove(0);
        }
    }
}

/// Resolve the tredo workspace root directory.
fn project_root() -> std::path::PathBuf {
    let cwd = std::env::current_dir().unwrap_or_else(|_| std::path::PathBuf::from("."));
    if cwd.join("kronos_service").exists() {
        return cwd.clone();
    }
    if let Ok(exe) = std::env::current_exe() {
        let mut dir = exe
            .parent()
            .unwrap_or(std::path::Path::new("."))
            .to_path_buf();
        for _ in 0..6 {
            if dir.join("kronos_service").exists() {
                return dir;
            }
            dir = dir
                .parent()
                .unwrap_or(std::path::Path::new("."))
                .to_path_buf();
        }
    }
    cwd
}

